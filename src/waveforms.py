# ============================================================
# WaveformPlayer — 波形播放器
# ============================================================

class WaveformPlayer:
    """管理单个通道的波形播放

    用法:
        player = WaveformPlayer("呼吸")
        while playing:
            pulse = player.next_pulse()
            await client.add_pulses(channel, pulse)
    """

    def __init__(self, waveform: str = "恒定"):
        self._index = 0
        self._data: List[Pulse] = []
        self.set_waveform(waveform)

    def set_waveform(self, name: str):
        """切换波形（保留当前播放位置）"""
        if name == "恒定" or name not in PULSE_DATA:
            self._data = []
        else:
            self._data = PULSE_DATA[name]
        self._index = 0

    def next_pulse(self) -> Optional[Pulse]:
        """获取下一条 pulse，自动循环"""
        if not self._data:
            return None
        pulse = self._data[self._index]
        self._index = (self._index + 1) % len(self._data)
        return pulse

    def reset(self):
        """重置播放位置到开头"""
        self._index = 0

    @property
    def is_constant(self) -> bool:
        return len(self._data) == 0

    @property
    def current_name(self) -> str:
        if not self._data:
            return "恒定"
        # 反向查找名称
        for name, data in PULSE_DATA.items():
            if data == self._data:
                return name
        return "未知"


# ============================================================
# 随机波形辅助
# ============================================================

def random_waveform_name(exclude: Optional[str] = None) -> str:
    """随机返回一个波形名称"""
    names = list(PULSE_DATA.keys())
    if exclude and exclude in names and len(names) > 1:
        names.remove(exclude)
    return random.choice(names)
